// "let" asigna valor a la variable
let numero = parseInt(prompt("Por favor, introduce un numero:"));
//Condicional si al dividirlo entre entre el residuo es 0
if(numero % 2 === 0){
    console.log(`${numero}es par`);
    //En caso de tener residuo sera impar
}else{
    console.log(`${numero}es impar`);
}

